package Operators;

public class IncrementDecrement {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        System.out.println("PrePost Incr..");
        System.out.println(a++);
        System.out.println(++a);

        System.out.println("PrePost Decre..");
        System.out.println(a--);
        System.out.println(--a);
    }
}
